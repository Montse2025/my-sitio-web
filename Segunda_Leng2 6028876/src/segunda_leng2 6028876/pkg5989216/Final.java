    private void carga() {
        String Cliente, Nombre, Apellido, Sexo;
	    Nombre = jTnombre.getText ();
	    Apellido = jTapellido.getText ();
	    Sexo = String.valueOf (jCsexo.getSelectedItem ());
	    Cliente = Nombre + " » " + Apellido + " » " + Sexo;
            
            if (jRcredito.isSelected () && jRcheque.isSelected ())
	    {
		  CreditoCheque.addElement (Cliente);
		  jCcrediCheque.setModel (CreditoCheque);
		  TCREDIandCHE++;

	    } else if (jRcontado.isSelected () && jRefectivo.isSelected ())
	    {
		  ContadoEfectivo.addElement (Cliente);
		  jLcontadoEfectivo.setModel (ContadoEfectivo);
		  TCONTandEfec++;

	    }
	    if (jRcontado.isSelected ())
	    {
		  Cont++;
		  if (jCsexo.getSelectedItem ().equals ("F"))
		  {
			ContF++;
		  }
	    }
	    if (jRcheque.isSelected ())
	    {
		  Chec++;
		  if (jCsexo.getSelectedItem ().equals ("M"))
		  {
			CheM++;
		  }
	    }
	    if (jRcredito.isSelected ())
	    {
		  Credi++;
	    }
	    if (jRefectivo.isSelected ())
	    {
		  Efec++;
	    }

	    jLtotalContadoEfectivo.setText (String.valueOf (TCONTandEfec));
	    jLtotalCreditoCheque.setText (String.valueOf (TCREDIandCHE));
	    jLVentaContado.setText (String.valueOf (Cont));
	    jLContadoMujer.setText (String.valueOf (ContF));
	    jLtotalAcumuladoCheque.setText (String.valueOf (Chec));
	    jLChequeHombre.setText (String.valueOf (CheM));
	    jLVentaCredito.setText (String.valueOf (Credi));
	    jLCantPagadosEfectivo.setText (String.valueOf (Efec));

	    TGLOBAL = Cont + Credi;
	    jStotal.setValue (TGLOBAL);
    }
