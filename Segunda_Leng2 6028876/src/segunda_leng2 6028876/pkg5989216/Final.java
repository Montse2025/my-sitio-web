    private void jBconcretarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconcretarActionPerformed
        if (JTcedula.getText ().isEmpty () == true)
        {
            JOptionPane.showMessageDialog (this, "El campo cedula no puede estar vacio", "ERROR", HEIGHT);
            JTcedula.requestFocus ();
        } else if (jTnombre.getText ().isEmpty () == true)
        {
            JOptionPane.showMessageDialog (this, "El campo nombre no puede estar vacio", "ERROR", HEIGHT);
            jTnombre.requestFocus ();
        } else if (jTapellido.getText ().isEmpty () == true)
        {
            JOptionPane.showMessageDialog (this, "El campo apellido no puede estar vacio", "ERROR", HEIGHT);
            jTapellido.requestFocus ();
        } else if (jTvalorTotal.getText ().isEmpty () == true)
        {
            JOptionPane.showMessageDialog (this, "El campo Valor Total no puede estar vacio", "ERROR", HEIGHT);
            jTvalorTotal.requestFocus ();
        } else if (Integer.valueOf (jSedad.getValue ().toString ()) <= 18)
        {
            JOptionPane.showMessageDialog (this, "Debe ser mayor de edad", "ERROR", HEIGHT);
            jSedad.requestFocus ();

        } else if (jCsexo.getSelectedItem ().equals ("Seleccione..."))
        {
            JOptionPane.showMessageDialog (this, "Defina el sexo", "ERROR", HEIGHT);
        } else if (!jRcheque.isSelected () && !jRefectivo.isSelected ())
        {
            JOptionPane.showMessageDialog (this, "Seleccione un metodo de Pago", "ERROR", HEIGHT);
        }else if(!jRcontado.isSelected () && !jRcredito.isSelected ()){
            JOptionPane.showMessageDialog (this, "Seleccione una condición de Venta", "ERROR", HEIGHT);
        }else
        {
            carga ();
        }
    }//GEN-LAST:event_jBconcretarActionPerformed

